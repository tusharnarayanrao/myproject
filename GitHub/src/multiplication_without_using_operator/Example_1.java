package multiplication_without_using_operator;

public class Example_1
{public static void main(String[] args) {
	


	int a = 2 ;
	 int b = 3 ;
	 
	 int sum = 0 ;

	 for(int i = 1 ; i<=b ; i++) 
	 {
		 //   2  =   0+ 2
		 //   4  = 2+2
		 //   6  =  4+2
		sum = sum + a; 
		 
		 
	 }
	 System.out.println(sum);
}
}